<?php
class ControllerClientes{
    	/*=============================================
	Mostrar clientes
    =============================================*/
    public static function ControladorMostrarClientes($item, $valor){
        $tabla = "clientes";//envio como parametro la tabla clientes
        //envio una respuesta al modelo enviando los parametros el item y el valor
        //en este caso es null
		$respuesta = ModeloClientes::ModelMostrarClientes($tabla, $item, $valor);

		return $respuesta;   
    }
    	/*=============================================
	Ingresar clientes
    =============================================*/
    public static function ControladorIngresarClientes(){
        if(isset($_POST["nuevoCliente"])){//evaluamos si nos viene una peticion post

			if(preg_match('/^[a-zA-Z0-9ñÑáéíóúÁÉÍÓÚ ]+$/', $_POST["nuevoCliente"]) &&
			   preg_match('/^[0-9]+$/', $_POST["nuevoDocumentoId"]) &&
			   preg_match('/^[^0-9][a-zA-Z0-9_]+([.][a-zA-Z0-9_]+)*[@][a-zA-Z0-9_]+([.][a-zA-Z0-9_]+)*[.][a-zA-Z]{2,4}$/', $_POST["nuevoEmail"]) && 
			   preg_match('/^[()\-0-9 ]+$/', $_POST["nuevoTelefono"]) && 
			   preg_match('/^[#\.\-a-zA-Z0-9 ]+$/', $_POST["nuevaDireccion"])){
                //validamos los datos para cada input
			   	$tabla = "clientes";//enviamos la tabla

			   	$datos = array("nombre"=>$_POST["nuevoCliente"], //colocamos los valores en un array
					           "documento"=>$_POST["nuevoDocumentoId"],
					           "email"=>$_POST["nuevoEmail"],
					           "telefono"=>$_POST["nuevoTelefono"],
					           "direccion"=>$_POST["nuevaDireccion"],
					           "fecha_nacimiento"=>$_POST["nuevaFechaNacimiento"]);

			   	$respuesta = ModeloClientes::ModelIngresarClientes($tabla, $datos);

			   	if($respuesta == "ok"){

					echo'<script>

					swal({
						  type: "success",
						  title: "El cliente ha sido guardado correctamente",
						  showConfirmButton: true,
						  confirmButtonText: "Cerrar",
						  closeOnConfirm: false
						  }).then((result) => {
									if (result.value) {

									window.location = "clientes";

									}
								})

					</script>';

				}

			}else{

				echo'<script>

					swal({
						  type: "error",
						  title: "¡El cliente no puede ir vacío o llevar caracteres especiales!",
						  showConfirmButton: true,
						  confirmButtonText: "Cerrar",
						  closeOnConfirm: false
						  }).then((result) => {
							if (result.value) {

							window.location = "clientes";

							}
						})

			  	</script>';



			}

		}

    }
